package comp4104.imsg.core.net;

/**
 * Models a request that can be sent to/from the
 * server and clients. 
 */
public interface IRequest extends IRequestResponse
{
	/**
	 * Gets the command.
	 * @return The command string.
	 */
	public String getCommand();
	
	/**
	 * Sets the command.
	 * @param command
	 * 		  The command string.
	 */
	public void setCommand(String command);	
}