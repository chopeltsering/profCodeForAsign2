package comp4104.imsg.core.net;

/**
 * A model of a request / response writer.
 */
public interface IRequestResponseWriter
{
	/**
	 * Writes a request. 
	 * @param request
	 * 		  The <code>IRequest</code> to write.
	 * @throws Exception
	 */
	public void writeRequest(IRequest request) throws Exception;
	
	/**
	 * Writes a response.
	 * @param response
	 * 		  The <code>IResponse</code> to write.
	 * @throws Exception
	 */
	public void writeResponse(IResponse response) throws Exception;
}